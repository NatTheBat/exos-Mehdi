def concat_and_display amanger, aboire
	puts 'Servez-moi immédiatement ' + amanger + ' ' + 'et aussi ' + aboire
end

concat_and_display 'des frites', 'de la bière'
concat_and_display 'du caviar', 'de la vodka'
concat_and_display 'une salade', 'de la badoit'
concat_and_display 'un big mac', 'un lait grenadine'